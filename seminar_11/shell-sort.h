#ifndef OS_3RDTERM_SHELL_SORT_H
#define OS_3RDTERM_SHELL_SORT_H

void shellSort(int *arr, int n) {
    for (int gap = n/2; gap > 0; gap /= 2) {
        for (int i = gap; i < n; i++) {
            int temp = arr[i];
            int j;
            for (j = i; j >= gap && arr[j - gap] > temp; j -= gap) {
                arr[j] = arr[j - gap];
            }
            arr[j] = temp;
        }
    }
}

#endif //OS_3RDTERM_SHELL_SORT_H
